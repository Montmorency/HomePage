      SUBROUTINE RECQD(A,B2,LM1,X,WT,M,EPS,WK,MU)

      DIMENSIONA(LM1),B2(LM1),X(LM1),WT(LM1),WK(LM1),MU(LM1)

      DIMENSION P(2,3)

      M=1

      CALL RECRTS(A,B2,LM1,EPS,XLIM,M,X,MU,WK,WT)

      DO 7 II=1,M

      WT(II)=RECWT(X(II),A,B2,LM1,EPS,0,P,1)

7     CONTINUE

      RETURN

      END
